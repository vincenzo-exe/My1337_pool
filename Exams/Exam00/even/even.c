#include <unistd.h>

int	main(void)
{
	int		i;
	char	c;

	i = 0;
	while (i <= 10)
	{
		if (i % 2 == 0)
		{
			if (i == 10)
				write(1, "10", 2);
			else
			{
				c = i + '0';
				write(1, &c, 1);
			}
		}
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
